<?php
    // Switch case

    $month = 2;

    switch ($month) {
        case 1:
            echo "มกราคม";
            break;
        case 2:
            echo "กุมภา";
            break;
        case 3:
            echo "มีนา";
            break;
        
        default:
            echo "เดือนไม่ถูก";
            break;
    }

?>